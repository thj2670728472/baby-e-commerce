async function initBabyCulture() {
  try {
    // 检查是否已有数据
    const count = await baby_culture.count();
    if (count > 0) {
      console.log(`数据库中已有 ${count} 条婴儿培养文章`);
      return;
    }

    // 插入示例数据
    await baby_culture.bulkCreate(sampleArticles);
    console.log('已添加8条婴儿培养文章');
  } catch (error) {
    console.error('初始化婴儿培养数据失败:', error);
  }
}

initBabyCulture();
